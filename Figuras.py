class Figuras():

   def cuadrado(self, num1, num2):
      return num1*num2
   def circulo(self,radio):
       return (3.1416*(radio*radio))
   def rectanculo(self,num1, num2):
       return num1*num2
   def triangulo(self,b,a):
       return (b*a)/2
   def elipse(self,la,la2):
       return (3.1416*(la*la2))
